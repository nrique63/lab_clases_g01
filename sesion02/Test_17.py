"""Listas en python"""
"""listan len() : obtener el tamaño de la lista"""
paises = []
print("el tamaño de la lista es : {}".format(len(paises)))
"""agregando datos a mi lista"""
paises.append("Perú")
paises.append("Irlanda")
paises.append("Brazil")
paises.append("Suecia")
print("el nuevo tamaño de la lista es : {}".format(len(paises)))
print(paises)
