"""Listas en Python"""

"""Listas pop(): quita un elemento de una poblacion dada"""
paises = ["Francia", "Canada", "Alemania", "Irlanda"]
print("Mi lista inicial: {}".format(paises))
paises.pop()
print("Mi lista actualizada es {}".format(paises))
print("El pais 2 es {}".format(paises[2]))
paises.pop(2)
print("Mi lista actualizada es {}".format(paises))